#!/usr/bin/env node
// EXAMPLE — Claude Code PreToolUse hook (opt-in, vendor-specific): block edits to files that
// nbp-skillforge GENERATED, shifting the drift-gate left — the agent is stopped the moment it tries
// to edit a generated skill, before the commit-time hook would catch it. Edit the recipe instead.
//
// Wire it via .claude/settings.json (see settings.json next to this file). Zero deps.
// Exit 2 = block the tool call and feed the message back to the agent; exit 0 = allow.

import { readFileSync, existsSync, statSync } from "node:fs";
import { resolve, isAbsolute } from "node:path";

let input = "";
let done = false;
process.stdin.on("data", (c) => { input += c; if (input.length > 1_000_000) decide(); }); // bound stdin
process.stdin.on("end", decide);

function decide() {
  if (done) return;
  done = true;
  let rel, cwd;
  try { const j = JSON.parse(input); rel = j?.tool_input?.file_path ?? j?.tool_input?.path; cwd = j?.cwd; }
  catch { process.exit(0); } // not JSON we understand → don't get in the way
  if (!rel) process.exit(0);

  const path = isAbsolute(rel) ? rel : resolve(cwd || process.cwd(), rel);
  if (!existsSync(path) || !statSync(path).isFile()) process.exit(0);

  let text = "";
  try { text = readFileSync(path, "utf8"); } catch { process.exit(0); }
  // The banner sits at column 0 of its own line (top of file, or right after frontmatter), so a
  // line-anchored match catches generated files while ignoring inline mentions in docs/tests.
  // Tolerant to the pre-rename name (`nbp-forge`) — mirrors GENERATED_BANNER_RE in src/compose.mjs,
  // so files generated before the nbp-skillforge rename are still protected from hand-edits.
  if (/^<!-- GENERATED by nbp-(?:skill)?forge/m.test(text)) {
    process.stderr.write(
      `Refusing to edit a GENERATED file: ${path}\n` +
      "This is nbp-skillforge build output. Edit the recipe (and any brick it includes), then run `forge build`.\n"
    );
    process.exit(2);
  }
  process.exit(0);
}
