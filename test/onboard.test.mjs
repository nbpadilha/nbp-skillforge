// One hostile-mix fixture reused by several tests.
const mixRoot = () => {
  const root = makeRoot({
    bricks: { b: "brick body" },
    recipes: { governed: "---\nname: governed\ndescription: d.\n---\n# governed\n\n<!-- include: b -->\n" },
  });
  run({ root, mode: "build" }); // `governed` now sits generated in out/
  write(join(root, "out", "hand-made.md"), "---\nname: hand-made\ndescription: Hand-written skill.\n---\n# hand-made\n\nDo the thing.\n");
  write(join(root, "out", "plain-cmd.md"), "# plain command\n\nNo frontmatter at all.\n");
  write(join(root, "out", "tool.md"), `---\nname: tool\ndescription: t.\nforge-role: ${FORGE_ROLE_VALUE}\n---\n# tool\nbody\n`);
  write(join(root, "out", "Bad_Name.md"), "---\nname: Bad_Name\ndescription: d.\n---\n# bad\nbody\n");
  write(join(root, "out", "governed.md"), read(join(root, "out", "governed.md"))); // unchanged, still generated
  write(join(root, "out", "has-include.md"), "# x\n\n<!-- include: run-dir -->\n");
  write(join(root, "out", "fenced-include.md"), "# y\n\n```\n<!-- include: run-dir -->\n```\n");
  writeFileSync(join(root, "out", "binary.md"), Buffer.from([0xff, 0xfe, 0x00, 0x41, 0x80, 0x81]));
  mkdirSync(join(root, "out", "sub"), { recursive: true });
  write(join(root, "out", "sub", "nested.md"), "# nested\n");
  return root;
};

test("discover: classifies the hostile mix correctly, every file gets a disposition", () => {
  const root = mixRoot();
  try {
    const { entries } = discover(root, loadConfig(root));
    const by = Object.fromEntries(entries.map((e) => [e.file.replace(/\\/g, "/").split("/").pop(), e.status]));
    assert.equal(by["hand-made.md"], "eligible");
    assert.equal(by["plain-cmd.md"], "eligible", "a no-frontmatter command is eligible (no name gate)");
    assert.equal(by["fenced-include.md"], "eligible", "an include inside a fence is NOT a landmine (engine parity)");
    assert.equal(by["governed.md"], "excluded-generated", "banner detected AFTER splitFm (fm'd file)");
    assert.equal(by["tool.md"], "excluded-forge-role");
    assert.equal(by["Bad_Name.md"], "skip-nonconformant");
    assert.equal(by["has-include.md"], "skip-include-like");
    assert.equal(by["binary.md"], "skip-non-utf8");
    assert.equal(by["nested.md"], "skip-nested");
    const bad = entries.find((e) => e.status === "skip-nonconformant");
    assert.match(bad.proposal ?? "", /bad-name/, "a conformant rename is proposed, never applied");
  } finally { cleanup(root); }
});

