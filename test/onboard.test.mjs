test("preScan: BOM detected+stripped; placeholders recorded; include-like honors fences", () => {
  // ﻿ escape (not a literal BOM char) — editors/shells silently strip the literal.
  const bom = preScan(Buffer.from("﻿---\nname: x\n---\nbody {{param}}\n", "utf8"));
  assert.equal(bom.bom, true);
  assert.match(bom.text, /^---/, "BOM stripped so splitFm's anchor works");
  assert.deepEqual(bom.placeholders, ["{{param}}"]);
  const inv = preScan(Buffer.from([0xc3, 0x28])); // invalid UTF-8 sequence
  assert.equal(inv.utf8, false);
});

