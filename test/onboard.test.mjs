// F-33 parity: a bang directive (`include!:`) is expanded by the engine even INSIDE a fence, so
// the pre-scan must flag it as include-like wherever it sits — a fence no longer proves safety.
// Athena triage (proven by execution): the OLD single reason told the fenced-bang author to
// "fence it" — advice already satisfied, a permanent dead end. The KIND now travels with the
// scan ("bang" | "unfenced") and the reason must name the actual case and a remedy that works.
test("discover: a bang include inside a fence is skip-include-like with a BANG-specific reason", () => {
  const root = makeRoot({});
  write(join(root, "out", "bang-fenced.md"), "# z\n\n```\n<!-- include!: run-dir -->\n```\n");
  try {
    const scan = preScan(readRaw(join(root, "out", "bang-fenced.md")));
    assert.equal(scan.includeLike, "bang", "preScan must flag a fenced BANG directive as include-like, KIND = bang");
    const { entries } = discover(root, loadConfig(root));
    const e = entries.find((x) => x.file.replace(/\\/g, "/").endsWith("bang-fenced.md"));
    assert.equal(e.status, "skip-include-like");
    assert.match(e.reason, /bang include directive/, "the reason names the bang case");
    assert.match(e.reason, /expands it even inside a code fence/, "the reason states the fence is no protection");
    assert.match(e.reason, /remove the `!`/, "the advice is actionable for a bang");
    assert.ok(!e.reason.includes("fence it"), "the dead-end 'fence it' advice must never reach a bang author");
    assert.ok(!e.reason.includes("outside a code fence"), "the factually-wrong 'outside a fence' claim is gone");
  } finally { cleanup(root); }
});

