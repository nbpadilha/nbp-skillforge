test("onboard --apply: E2E — snapshot faithful, recipes created, gate zero, check green, report covers all", () => {
  const root = mixRoot();
  try {
    const originalCrlf = "---\r\nname: crlf-skill\r\ndescription: d.\r\n---\r\n# crlf\r\n\r\nCRLF body\r\n";
    writeFileSync(join(root, "out", "crlf-skill.md"), originalCrlf);
    const r = onboard({ root, ts: TS, apply: true });
    assert.equal(r.ok, true, r.msg);
    // (1) backup byte-faithful (CRLF preserved)
    const bak = join(root, "_onboard-backup-" + TS);
    assert.equal(readFileSync(join(bak, "crlf-skill.md"), "latin1"), Buffer.from(originalCrlf).toString("latin1"), "backup preserves exact original bytes");
    // (2) recipes exist + gate passed for every migrated skill
    for (const s of ["hand-made", "plain-cmd", "crlf-skill", "fenced-include"]) {
      assert.equal(has(recipe(root, s)), true, `recipe created: ${s}`);
      assert.equal(r.gate.find((g) => g.skill === s)?.pass, true, `gate zero for ${s}`);
    }
    // rebuilt output is LF-only even though the source was CRLF
    assert.equal(readRaw(outFile(root, "crlf-skill")).includes(0x0d), false, "out is LF");
    // (3) drift-gate green afterward
    assert.equal(run({ root, mode: "check" }).ok, true, "forge check green after onboarding");
    // (4) report covers 100% of the scanned files
    const report = read(join(bak, "onboard-report.md"));
    for (const f of ["hand-made.md", "governed.md", "tool.md", "Bad_Name.md", "has-include.md", "binary.md", "nested.md"])
      assert.ok(report.includes(f), `report mentions ${f}`);
    // Release-readiness fix (2026-07-07): the fidelity-gate verdict cell is EN like every other
    // artifact of this repo — the old PT cell ("✔ fiel (diff normalizado zero)") must never return.
    assert.match(report, /✔ faithful \(normalized diff zero\)/, "gate verdict rendered in EN");
    assert.doesNotMatch(report, /fiel \(diff normalizado zero\)/, "no PT leftover in the report");
    // skips present → enforceGenerated must NOT auto-enable
    assert.equal(r.enforced, false);
    assert.equal(loadConfig(root).enforceGenerated, false);
    assert.match(report, /enforceGenerated stays OFF/);
    // (5) skipped/excluded originals untouched
    assert.match(read(join(root, "out", "Bad_Name.md")), /# bad/, "skipped file untouched");
  } finally { cleanup(root); }
});

// Athena triage F2 (proven by execution): a user skill literally named `onboard-report.md` lands
// in the backup dir as a byte-faithful snapshot BEFORE writeReport() runs — the report used to
// overwrite it, silently destroying the only byte-exact copy of the original, and the report's own
// rollback instruction would then restore the REPORT TEXT over the user's skill. The guard must
// keep the snapshot intact and divert the report to the first free `onboard-report-<n>.md`.
test("onboard --apply: a user skill named 'onboard-report' never loses its snapshot to the report", () => {
  const root = makeRoot({});
  // Distinctive bytes: CRLF + trailing spaces — exactly what the recipe normalizes away, so the
  // snapshot is the ONLY byte-exact copy (the execution repro's fidelity-loss scenario).
  const originalBytes = "---\r\nname: onboard-report\r\ndescription: user skill, unlucky name.\r\n---\r\n# onboard-report\r\n\r\nline1  \r\nline2\r\n";
  mkdirSync(join(root, "out"), { recursive: true }); // raw writeFileSync (exact bytes) — no helper mkdir
  writeFileSync(join(root, "out", "onboard-report.md"), originalBytes);
  try {
    const r = onboard({ root, ts: TS, apply: true });
    assert.equal(r.ok, true, r.msg);
    const bak = join(root, "_onboard-backup-" + TS);
    // (1) the snapshot survives byte-for-byte — never overwritten by the report
    assert.equal(readRaw(join(bak, "onboard-report.md")).equals(Buffer.from(originalBytes)), true,
      "the byte-faithful snapshot of the user's skill must survive writeReport()");
    // (2) the report itself was diverted to the first free suffixed name, content intact
    assert.equal(has(join(bak, "onboard-report-2.md")), true, "the report falls back to onboard-report-2.md");
    assert.match(read(join(bak, "onboard-report-2.md")), /^# onboard report/, "the diverted file IS the report");
    // (3) the skill was genuinely onboarded (recipe exists, gate passed)
    assert.equal(has(recipe(root, "onboard-report")), true);
    assert.equal(r.gate.find((g) => g.skill === "onboard-report")?.pass, true);
  } finally { cleanup(root); }
});

