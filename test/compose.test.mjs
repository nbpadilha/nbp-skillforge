test("build: missing brick → build error and NOTHING written", () => {
  const root = makeRoot({
    recipes: { x: "---\nname: x\n---\n# x\n\n<!-- include: nope -->\n" },
  });
  try {
    const r = run({ root, mode: "build" });
    assert.equal(r.ok, false);
    assert.equal(r.written, 0);
    assert.ok(r.errors.some((e) => e.startsWith("build error:") && /missing brick: nope/.test(e)));
    assert.equal(has(outFile(root, "x")), false, "no output file should exist on build error");
  } finally { cleanup(root); }
});

