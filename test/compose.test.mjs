// compose / build / check — the load-bearing engine.
import { test } from "node:test";
import assert from "node:assert/strict";
import { run } from "../src/compose.mjs";
import { makeRoot, read, readRaw, has, outFile, cleanup, write } from "./helpers.mjs";

test("build: simple include inlines the brick body", () => {
  const root = makeRoot({
    bricks: { greeting: "Hello from the brick." },
    recipes: { hi: "---\nname: hi\n---\n# hi\n\n<!-- include: greeting -->\n" },
  });
  try {
    const r = run({ root, mode: "build" });
    assert.equal(r.ok, true, r.errors?.join("; "));
    assert.equal(r.written, 1);
    const txt = read(outFile(root, "hi"));
    assert.match(txt, /Hello from the brick\./);
  } finally { cleanup(root); }
});

