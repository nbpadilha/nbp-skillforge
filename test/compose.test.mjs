// F-18: the symmetric case — the on-disk file is SHORTER than the built content (a hand-edit
// truncated/removed trailing lines) — must report "found end-of-file", not crash or mis-point.
test("check: drift message reports 'found end-of-file' when the on-disk file was truncated", () => {
  const root = makeRoot({
    bricks: { b: "line one\nline two\nline three" },
    recipes: { d: "---\nname: d\n---\n# d\n\n<!-- include: b -->\n" },
  });
  try {
    run({ root, mode: "build" });
    const f = outFile(root, "d");
    const builtLines = read(f).split("\n");
    // Keep every line up to (and including) "line two" verbatim, but drop the final "line three"
    // line AND the trailing newline itself (not just its content) — so the on-disk array is
    // genuinely SHORTER than the built array (no trailing "" element to mask the truncation).
    const cutAt = builtLines.indexOf("line two") + 1;
    write(f, builtLines.slice(0, cutAt).join("\n"));
    const r = run({ root, mode: "check" });
    assert.equal(r.ok, false);
    const e = r.errors.find((e) => e.kind === "drift");
    assert.match(e.msg, /found end-of-file/);
    assert.match(e.msg, /expected "line three"/);
  } finally { cleanup(root); }
});

