// ── T2: pin the matchAll (not .test()/.exec()) invariant for the nested-include gate ───────────
// The gate must use `[...b.matchAll(INCLUDE)]`, never `INCLUDE.test(b)`, on the shared MODULE-
// LEVEL `INCLUDE` regex — `.test()`/`.exec()` with the `g` flag mutate `INCLUDE.lastIndex` as a
// side effect; `.matchAll()`'s spec-mandated clone SEEDS its copy's lastIndex from the original's
// (still-dirty) lastIndex, so a later, UNRELATED `includesOf()`/`matchAll()` call in the same
// process can silently start scanning past a real include near the start of a short text and miss
// it. A simple "does the nested-include case still work" test would NOT catch a regression back
// to `.test()` — it passes either way. This test needs a SEPARATE, later matchAll consumer
// (includesOf) in the SAME process to actually exercise the shared-regex leak.
test("F-01: the nested-include gate does not dirty the shared INCLUDE regex for a LATER includesOf() call", () => {
  const root = makeRoot({
    bricks: {
      wrapper: "<!-- include: inner -->\n", // brick-in-brick: triggers the nested-include gate
      inner: "inner content",
    },
    recipes: { broken: "---\nname: broken\n---\n# broken\n\n<!-- include: wrapper -->\n" },
  });
  try {
    const r = run({ root, mode: "build" });
    assert.equal(r.ok, false, "the nested include must still be a build error");
    assert.ok(r.errors.some((e) => /nested include/.test(e.msg)));
  } finally { cleanup(root); }
  // A SEPARATE, short text with a real include at the very start (offset 0), processed AFTER the
  // nested-include build above, in the same process (same shared module-level INCLUDE regex).
  assert.deepEqual(includesOf("<!-- include: real -->\n"), ["real"],
    "a real include right at the start of a later, unrelated text must still be found — the shared INCLUDE regex's lastIndex must not have leaked");
});

